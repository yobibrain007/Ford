subset([H|T] , ST):-
  member( ST , H),
  subset( T , ST).

member( [H|_] , H):-!.
member( [_|T] , I ):-
	member(T , I).

findrule(ID, FTerm, MentionList, Relation):-
	rule(ID, [FTerm|L], Relation),
	subset([FTerm|L], MentionList).


findMatchedMentions(MentionsOfMentionId, Mentions, MatchedItems):-
	matchMentions(MentionsOfMentionId, Mentions, [], MatchedItems).

matchMentions([],_, MatchedItems, MatchedItems):- !.

matchMentions([FTerm|T], MentionList, L, MatchedItems):-
	findrule(MatchedCondID, FTerm, MentionList, Relation),!,
	%H = matchedItem(FTerm, MatchedCondID, Relation),
	H = Relation,
	matchMentions(T, MentionList, [H|L], MatchedItems).

matchMentions([_|T],MentionList,  L, MatchedItems):-
	matchMentions(T, MentionList, L, MatchedItems).


